# Ruby Array Exercises: Remove duplicate elements from a given array


nums = [1, 2, 3, 4, 1, 2, 2, 3, 5, 6]
print "Original array:\n"
print nums
print "\n Array with unique elements:\n"
new_nums = nums.uniq
print new_nums



# output===

# Original array:
# [1, 2, 3, 4, 1, 2, 2, 3, 5, 6]
#  Array with unique elements:
# [1, 2, 3, 4, 5, 6]
